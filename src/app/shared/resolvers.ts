import { Response } from '@angular/http';
import { Injectable } from '@angular/core';
import { BackendService } from './backend.service';
import { Observable } from 'rxjs/Observable';
import { Resolve, RouterStateSnapshot, ActivatedRouteSnapshot } from '@angular/router';

@Injectable()
export class RecipesResolver implements Resolve<Response> {
  constructor(private backendService: BackendService) {}

  resolve(route: ActivatedRouteSnapshot, state: RouterStateSnapshot): Response | Observable<Response> | Promise<Response> {
    return this.backendService.fetchRecipes();
  }
}
